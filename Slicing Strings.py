#REMEMBER The first character has index 0.

#Slicing from the START Index

b = "Hello, World!"
print(b[:5])

#Slicing from the END Index

b = "Hello, World!"
print(b[2:])

b = "Hello, World!"
print(b[5:])
print(b[6:])
print(b[7:])


